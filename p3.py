def generate_workout_plan(workouts):
    workout_plan = "Your daily workout plan for the next 15 days is:\n\n"
    random.shuffle(workouts)
    for i, workout in enumerate(workouts, start=1):
        if workout in ["Push ups", "Bench press", "Triceps Pushdown", "Biceps curl", "Overhead Tricep with dumbbells",
                       "Incline Bicep Curls", "Incline Bench press"]:
            workout_plan += f"Exercise {i} (Chest/Arms): {workout}\n"
        elif workout in ["Squats", "Lunges", "Leg extensions"]:
            workout_plan += f"Exercise {i} (Legs): {workout}\n"
        elif workout in ["Pull ups", "Lat pulldown", "Seated back rows", "Dumbbell shrugs"]:
            workout_plan += f"Exercise {i} (Back/Shoulders): {workout}\n"
        else:
            workout_plan += f"Exercise {i} (Cardio): {workout}\n"

    return workout_plan

